public class swapping {
    public static void main(String[] args) {
        int num1=24,num2=34;
        // assign number1 to temp
        int temp = num1;
        // assign num2 to num1
        num2=num1;
        // assign num1 to num2
        num2=temp;
        
        System.out.println("num1: " +num1);
        System.out.println("num2: " +num2);

    }
}